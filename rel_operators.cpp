#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 5;
    // The parentheses are important for cout
    cout << "Is a > b? " << (a > b) << endl;
    cout << "Is a == 10? " << (a == 10) << endl;
    //In c++, true and false are represented by 1 and 0 respectively
    return 0;
}
